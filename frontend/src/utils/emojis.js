export function getEmojiUrl(name) {
  const emoji = EMOJI_GG_REACTIONS.find(e => e.name === name);
  return emoji ? emoji.url : null;
}

export function getEmojiByCategory(category) {
  const categories = {
    pepe: EMOJI_GG_REACTIONS.filter(e => e.name.includes('pepe')),
    twitch: EMOJI_GG_REACTIONS.filter(e => ['pog', 'kek', 'lul', 'kappa', 'monka', 'sadge'].some(t => e.name.includes(t))),
    reactions: EMOJI_GG_REACTIONS.filter(e => ['chad', 'based', 'copium', 'hopium', 'despair', 'aware', 'bruh', 'clown', 'sus'].includes(e.name)),
    animals: EMOJI_GG_REACTIONS.filter(e => ['cat', 'dog', 'shiba'].some(t => e.name.includes(t))),
    positive: EMOJI_GG_REACTIONS.filter(e => ['heart', 'fire', '100', 'star', 'sparkles', 'rainbow'].includes(e.name)),
    negative: EMOJI_GG_REACTIONS.filter(e => ['skull', 'rip', 'dead', 'cry', 'angry', 'rage', 'ban', 'cringe', 'yikes'].includes(e.name)),
    gestures: EMOJI_GG_REACTIONS.filter(e => ['thumbs', 'clap', 'pray', 'ok', 'wave', 'salute', 'facepalm', 'shrug'].some(t => e.name.includes(t))),
    all: EMOJI_GG_REACTIONS
  };
  return categories[category] || categories.all;
}
